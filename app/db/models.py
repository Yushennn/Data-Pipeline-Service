class Athlete(base):
    __tablename__ = "athletes"

    athlete_id = Column(String, primary_key=True, index=True)
    name = Column(String, nullable=False)
    country = Column(String, nullable=False)
    
    match_performances = relationship("MatchPerformance", back_populates="athlete")
    set_performances = relationship("SetPerformance", back_populates="athlete")
    
class Match(base):
    __tablename__ = "matches"

    match_id = Column(String, primary_key=True, index=True)
    tournament = Column(String, nullable=False)
    weight_class = Column(String, nullable=False)
    round = Column(String, nullable=False)
    match_number = Column(String, nullable=False)
    
    match_performances = relationship("MatchPerformance", back_populates="match")
    set_performances = relationship("SetPerformance", back_populates="match")
    
